class FullDetails {
    constructor(userId, userName, role, email, phoneNumber, image, status) {
      this.userId = userId;
      this.userName = userName;
      this.role = role;
      this.email = email;
      this.phoneNumber = phoneNumber;
      this.image = image;
      this.status = status;
    }
  }
  
  module.exports = FullDetails;
  